{
	int i = (int)(12 + ((uint8_t)c) - 0xC0);
	if ((i < 0) || (i > font->glyph_cnt))
		i = 6;
	
	if (c == 0xaa)
		i = 0;
	if (c == 0xa8)
		i =  17;
	if (c == 0xb2)
		i =  2;
	if (c == 0xaf)
		i =  3;
	
	return &(font->glyph_dsc[i]);
}




